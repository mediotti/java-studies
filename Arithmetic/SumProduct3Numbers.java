public class SumProduct3Numbers {
    public static void main (String[] args) {
        int numberA = 98;
        int numberB = 5;
        int numberC = 77;

        int sum = numberA + numberB + numberC;
        int product = numberA * numberB * numberC;

        System.out.println("The sum of the 3 numbers: " + sum);
        System.out.println("The product of the 3 numbers: " + product);
    }
}
